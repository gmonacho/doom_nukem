#include "../includes/libft.h"

void	ft_2dstrdel(char ***tab)
{
	int		i;

	i = 0;
	if (*tab)
	{
		while ((*tab)[i])
			free((*tab)[i++]);
		free(*tab);
		*tab = NULL;
	}
}
